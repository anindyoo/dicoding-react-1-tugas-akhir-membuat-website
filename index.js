const generateTracklist = () => {
  const tracklistElement = document.getElementById('track-list');
  const liArray = tracklist.map((track) => {
    const li =
    `<li
      class="tracklist-item track-${track.id}"
      style="${track.id === 100 && 'grid-column: 1 / span 2'};"
    >
      <div
        class="track-detail"
        style="
          ${track.gap ? `gap: ${track.gap}` : ''};
        "
      >
        <h3
          class="font-${track.font}"
          style="
            font-size: ${track.fontSize};
            ${track.rotate ? `rotate: ${track.rotate}` : ''};
            ${track.top ? `top: ${track.top}` : ''};
            ${track.left ? `left: ${track.left}` : ''};
          "
        >${track.title}</h3>
        <p>${track.features ? `with ${track.features}` : '' }</p>
        <img
          src="./assets/images/tracklist/${track.id}.svg"
          alt="background for track #${track.id}"
          draggable="false"
        />
      </div>
    </li>`;
    return li;
  })
  tracklistElement.innerHTML = liArray.join(' ');
};
generateTracklist();

var scrollPosition = window.scrollY;
var fixedNav = document.getElementById('header-nav');

window.addEventListener('scroll', function() {
  scrollPosition = window.scrollY;

  if (scrollPosition >= window.innerHeight - 21) {
    fixedNav.classList.add('fixed-nav');
  } else {
    fixedNav.classList.remove('fixed-nav');
  }
});